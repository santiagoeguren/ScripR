###############################################################################
#Library
################################################################################

library(stabledist)



################################################################################
#Generar muestra
###############################################################################

#------------------------------------------------------------------------------
#Cargar parametros

n=1000
alpha=1.72
beta=0
gamma=1
delta=0
pm=2


var_sombrero=NULL


i=1

while (i<=1000) {
  
  var_sombrero[i]=var(rstable(n = n, alpha = alpha, beta = beta, gamma =  gamma, delta = delta ,pm=pm))
  
  i=i+1
}


################################################################################
#Estudio
################################################################################


#-------------------------------------------------------------------------------
#Graficar
#------------------------------------------------------------------------------

#serie de tiempo
plot(var_sombrero,type = "l")

#histograma
hist(var_sombrero)


#-------------------------------------------------------------------------------
#Calculos
#------------------------------------------------------------------------------
mean(var_sombrero)
sd(var_sombrero)


#-------------------------------------------------------------------------------
#Consistencia
#------------------------------------------------------------------------------


var_sombrero=NULL
muestra=NULL


i=1

while (i<=1000) {
  
  muestra[i]=rstable(n = 1, alpha = alpha, beta = beta, gamma =  gamma, delta = delta ,pm=pm)
  
  var_sombrero[i]=var(muestra)
  
  i=i+1
}

#-----------------------------------------------------------------------------
#graficar

plot(var_sombrero,type="l")
